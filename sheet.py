import openpyxl
# Writing Data
# Create a new workbook
wb = openpyxl.Workbook()
sheet = wb.active
sheet.title = "EmployeeData"
# Write data into cells
sheet['A1'] = "Name"
sheet['B1'] = "Department"
sheet['C1'] = "Salary"
sheet.append(["sasi", "HR", 30000])
sheet.append(["ravi", "IT", 40000])
sheet.append(["anil", "Finance", 35000])
# Save the workbook
wb.save("employee_data.xlsx")
print("Data written successfully to 'employee_data.xlsx'.")
# ---------- Reading Data ----------
# Load the existing workbook
wb2 = openpyxl.load_workbook("employee_data.xlsx")
sheet2 = wb2.active
print("\nReading data from 'employee_data.xlsx':")
for row in sheet2.iter_rows(values_only=True):
    print(row)